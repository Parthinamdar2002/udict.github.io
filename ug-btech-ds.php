        <div class="tab-pane fade" id="v-pills-eligibility" role="tabpanel" aria-labelledby="v-pills-eligibility-tab"><p><h3>Eligibility conditions and requirements for admissions</h3>
        <br>
<h5>1. Maharashtra State Candidate.</h5>
<br>
<li>The Candidate should be an Indian National and having domicile of Maharashtra state and/or born in Maharashtra state.</li>
<li>Passed HSC or its equivalent examination with Physics and Mathematics as compulsory subjects along with Chemistry / Computer Science / Electronic science / Information Technology / Biology / Informatics Practices / Biotechnology / Technical vocational subject / Agriculture / Engineering Graphics / Business Studies / Entrepreneurship and obtained at least 45% marks (at least 40% marks, in case of Backward class categories and Persons with Disability candidates belonging to Maharashtra State only) in the above subjects taken together and the candidate should have appeared in MGMU - CET 2022/ MHT-CET 2022/PERA CET 2022 and should obtain non zero score in MGMU - CET 2022/ MHT-CET 2020/PERA CET 2020. However, preference shall be given to the candidate obtaining non-zero positive score in MGMU - CET 2022 over the candidates who obtained non-zero score in MHT-CET 2022/ PERA-CET 2022.</li>
<center>OR</center>
<li>Passed Diploma in Engineering and Technology and obtained at least 45% marks (at least 40% marks, in case of Backward class categories and Persons with Disability candidates belonging to Maharashtra State only).</li>
<br>
<h5>2. All India Candidates</h5>
<br>
<li>The Candidate should be an Indian</li>
<li>Passed HSC or its equivalent examination with Physics and Mathematics as compulsory subjects along with Chemistry / Computer Science / Electronic science / Information Technology / Biology / Informatics Practices / Biotechnology / Technical vocational subject / Agriculture / Engineering Graphics / Business Studies / Entrepreneurship and obtained at least 45% marks (at least 40% marks, in case of Backward class categories and Persons with Disability candidates belonging to Maharashtra State only) in the above subjects taken together and candidate should have appeared in MGMU - CET 2022/ JEE Mains Paper-I/ MHT-CET 2022/ PERA-CET 2022 and should obtain non-zero score in MGMU - CET 2022/ JEE Mains Paper-I/ MHT-CET 2022/ PERA-CET 2022. However, preference shall be given to the candidate obtaining non-zero positive score in JEE Mains Paper-I over the candidates who obtained non-zero score in MGMU - CET 2022/ MHT-CET 2022/ PERA-CET 2022.</li>
<center>OR</center>
<li>Passed Diploma in Engineering and Technology and obtained at least 45% marks (at least 40% marks, in case of Backward class categories and Persons with Disability candidates belonging to Maharashtra State only).</li>  
<br>
<h5>3. NRI* Candidate</h5>
<br>
<li>The candidate should be an Indian National</li>
<li>The candidate should have passed HSC or its equivalent examination with Physics and Mathematics as compulsory subjects along with Chemistry / Computer Science / Electronic science / Information Technology / Biology / Informatics Practices / Biotechnology / Technical vocational subject / Agriculture / Engineering Graphics / Business Studies / Entrepreneurship and obtained at least 45% marks in the above subjects taken together.</li>
<h5>Note: *NRI Seats are based on AICTE norms</h5>
</p></div>

