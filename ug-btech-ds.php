          <p><h3>Program Outline</h3> 
          The B.Tech. in Data Science programme offers Outcome based education with Choice Based Credit System. The aim of this programme is to prepare a professionally competent student with technological leadership skills ready to work in a multidisciplinary work environment across the globe.</p>
<p>
The UDICT has taken a novel initiative in changing the paradigm of education in the recent scenario of Industry 4.0. In this scenario, there is a surge in the number of systems using numerical simulations and sensors. Petabytes of data are now being generated and collected through surveys, stock markets, sensors and simulations. Processing and extraction of meaningful information and knowledge from these datasets would require extensive use of graphical and tensor-based computing, statistical analysis and knowledge engineering for efficient estimation, prediction, forecasting, planning and decision support. The programme in Data Science intends to develop trained and quality human resource in these directions.
</p><p>
The program makes the students ready for positions in a wide array of industries and MNCs, as well as, opening the doors to the start-up universe. A lucid blend of fundamental and recent technologies is offered to the students for enhancing their skills for employability.
</p><p>
The Bachelor of Technology Programme intends to train the student not only on the techniques, but also on applying these techniques to several other disciplines like Civil, Mechanical, Electrical, Electronics, Chemical Engineering etc.</p>
       
