<h3>Meet the HOD</h3>
I welcome you on behalf of the University Department of Information and Communication Technology (UDICT), MGM University and congratulate you for choosing us to shape your bright future.
<br><br>

The UDICT is an offshoot of the Department of Information Technology of Jawaharlal Nehru Engineering College (JNEC). The department now offers modern and state-of-art undergraduate Programs in the domain of Information Technology (BTech IT) with special emphasis on Internet of Things (IoT), Blockchain, and Big Data Analytics (BDA), Artificial Intelligence and Machine Learning (B. Tech. AI and ML), and Post Graduate Diploma in AI and ML (PG Dip. AI and ML). In addition, the Department is currently offering the Master of Technology Degree in Geoinformatics (M. Tech. Geoinformatics).
<br><br>

It is indeed my pleasure and pride to head the UDICT. The undergraduate programs offered by UDICT has an intake of 60 for B. Tech. Information Technology (with verticals in IoT, Blockchain and Big Data Analytics), and an intake of 30 for B. Tech. Artificial Intelligence and Machine Learning (B. Tech. AI and ML).
<br><br>

The department has well qualified, highly experienced and motivated faculty members, who seek to combine excellence in education and research by producing budding technocrats with technical knowledge and value-based education. The department has highly motivated faculty who upgrades their skills according to the industry needs which helps them to prepare students industry ready. There is a high demand by the top IT companies for IT students to get recruited.
<br><br>

Historically, the Department has produced hundreds of professionals and has established a name for itself in the country and abroad. Our students have excelled in Higher Education, Government Jobs, Research and Development and Entrepreneurship. Information Technology finds applications in the areas of management information systems, geographic information system, information security analysis, database administration, network administration and management, cable tv, video on demand, multimedia conferencing, home shopping on the web, automated teller machine (ATM), electronic data interchange, telemedicine and many more in different fields.
<br><br>

Our department gives emphasis on teamwork approach, innovative teaching-learning methods and building leadership qualities into students. It also helps students to enhance their employability skills in collaboration with industry.
<br><br>

We believe that, all these activities make our students stronger to handle the challenges posed by the IT industry.
