        <h3>About UDICT</h3>
        
The University Department of Information and Communication Technology (UDICT) is an offshoot of the Department of Information Technology of Jawaharlal Nehru Engineering College (JNEC). The department now offers modern and state-of-art courses in the domain of Information Technology with special emphasis on Internet of Things (IoT), Blockchain, and Big Data Analytics (BDA), an undergraduate degree in the domain of Artificial Intelligence and Machine Learning (B.Tech. AI and ML), and Postgraduate Diploma in AI and ML (PG Dip. AI and ML). In addition, the Department is currently offering the Master of Technology Degree in Geoinformatics (M.Tech. Geoinformatics).
<br><br>

<h3>Vision</h3>

The UDICT shall establish a center-of-excellence with social and global approaches for the development of trained human resources with professional approaches to innovation, entrepreneurship, and knowledge of emergent technologies for industry and research in diverse domains.
<br><br>

<h3>Mission</h3>
<h4>The UDICT shall</h4>
<ul>
  <p> <li>Empower students with human values, environment-friendly qualities and ethical approaches.</li>  </p>
  <p> <li>Create technocrats for a sustainable growth in the industrial sector with an interdisciplinary aptitude.</li> </p>
  <p> <li>Hone their skills for becoming experts with state-of-art knowledge in social perspectives and research directions</</li> </p>
</ul>
