        <h3>About MGM University</h3>
“In a gentle way, you can shake the world.” With this philosophy and ideology of Mahatma Gandhi, MGM established an educational trust in 1982 under the guidance of former Education Minister Shri Kamalkishore Kadam and a group of founder members who are hard-core academicians and visionaries. The Trust established its pioneering institute in the historical city of Aurangabad, home to the heritage sites of Ajanta & Ellora but lacking in premiere educational institutes. For more than four decades, Mahatma Gandhi Mission has blazed a trail of academic excellence and state-of-the-art health care services. A strong dynamic institution that keeps pace with the demands of the modern world, prominent locations and excellent education facilities makes MGM worthwhile and an exciting choice for education. This institution is devoted to extensively impart education and enable the students to stand on their own, strongly believing that “Literary education is of no value if it is not able to build up a sound character.”
<br><br>

With the vision for imparting quality education MGM initiated a chain of institutes with a spectrum ranging from primary schools to post graduate degrees. MGM has been instrumental in promoting Classical Dance & Music, Art, Culture, Heritage and creativity for art seekers. The Trust has also paid heed to the aspect of physical fitness by converting a vast expanse of land into an ultra-modern facility catering to a multitude of sports. MGM’s lush green and serene campus is conducive to create a peaceful learning atmosphere.
<br><br>

It has acquired the reputation of being one of the premiere educational & health services charitable trust in India. In all, during this journey Mahatma Gandhi Mission has established its mark over 6 campuses having over 70 colleges, Institutions and Centers of Engineering, Architecture, Medical, Nursing, Management, Computer Science, IT, Biotechnology, Liberal Arts, Science, Commerce, Fine Arts, Performing Arts, Fashion Design, Mass Communication & Journalism, Film Arts and Photography.
<br><br>

The MGM Trust continues its journey of imparting value based services and creating global technocrats with its self – financed private “MGM University” launched at Aurangabad, Maharashtra with the approval of Government of Maharashtra and passing of MGM University Act 2019 by Maharashtra Legislative Assembly and Legislative Council.
